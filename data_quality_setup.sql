    -- Insert summary alert
    INSERT INTO DQ_COST_ALERTS_HISTORY (ALERT_TS, ALERT_TYPE, ALERT_LEVEL, OBJECT_NAME, MESSAGE, EMAIL_SENT)
    VALUES (CURRENT_TIMESTAMP(), 'DAILY_SUMMARY', 'INFO', NULL, :v_message, FALSE);

    -- Send summary email
    CALL PROC_SEND_COST_ALERT_EMAIL('INFO', NULL, :v_message);

    RETURN :v_message;
END;

-- ----------------------------------------------------------
-- Procedure 4: PROC_CHECK_BUDGET_ALERTS
-- Checks all objects against budget thresholds
-- ----------------------------------------------------------
CREATE OR REPLACE PROCEDURE PROC_CHECK_BUDGET_ALERTS()
RETURNS VARCHAR
LANGUAGE SQL
EXECUTE AS CALLER
AS
BEGIN
    LET v_alert_count NUMBER DEFAULT 0;

    -- Check each object against its budget config
    INSERT INTO DQ_COST_ALERTS_HISTORY (ALERT_TS, ALERT_TYPE, ALERT_LEVEL, OBJECT_NAME, MESSAGE, EMAIL_SENT)
    SELECT
        CURRENT_TIMESTAMP(),
        'BUDGET_EXCEEDED',
        CASE
            WHEN c.ESTIMATED_COST_USD >= b.DAILY_BUDGET_USD * b.CRITICAL_THRESHOLD_MULTIPLIER THEN 'CRITICAL'
            WHEN c.ESTIMATED_COST_USD >= b.DAILY_BUDGET_USD * b.WARNING_THRESHOLD_MULTIPLIER THEN 'WARNING'
        END,
        c.OBJECT_NAME,
        'Daily budget ' ||
        CASE
            WHEN c.ESTIMATED_COST_USD >= b.DAILY_BUDGET_USD * b.CRITICAL_THRESHOLD_MULTIPLIER THEN 'CRITICAL'
            ELSE 'warning'
        END ||
        ': spent $' || TO_VARCHAR(c.ESTIMATED_COST_USD, '999,999.999999') ||
        ' (' || TO_VARCHAR(ROUND(c.ESTIMATED_COST_USD / b.DAILY_BUDGET_USD * 100, 0)) || '% of $' ||
        TO_VARCHAR(b.DAILY_BUDGET_USD, '999,999.9999') || ' budget)',
        FALSE
    FROM DQ_COST_DAILY_LOG c
    INNER JOIN DQ_COST_BUDGET_CONFIG b ON c.OBJECT_NAME = b.OBJECT_NAME
    WHERE c.LOG_DATE = CURRENT_DATE()
      AND c.ESTIMATED_COST_USD >= b.DAILY_BUDGET_USD * b.WARNING_THRESHOLD_MULTIPLIER
      AND NOT EXISTS (
          SELECT 1 FROM DQ_COST_ALERTS_HISTORY ah
          WHERE ah.OBJECT_NAME = c.OBJECT_NAME
            AND ah.ALERT_TYPE = 'BUDGET_EXCEEDED'
            AND ah.ALERT_TS >= DATEADD(HOUR, -4, CURRENT_TIMESTAMP())
      );

    SELECT COUNT(*) INTO :v_alert_count
    FROM DQ_COST_ALERTS_HISTORY
    WHERE ALERT_TYPE = 'BUDGET_EXCEEDED'
      AND ALERT_TS >= DATEADD(MINUTE, -1, CURRENT_TIMESTAMP());

    -- Send emails for new budget alerts
    FOR alert_rec IN (
        SELECT ALERT_LEVEL, OBJECT_NAME, MESSAGE
        FROM DQ_COST_ALERTS_HISTORY
        WHERE ALERT_TYPE = 'BUDGET_EXCEEDED'
          AND EMAIL_SENT = FALSE
          AND ALERT_TS >= DATEADD(MINUTE, -1, CURRENT_TIMESTAMP())
    ) DO
        CALL PROC_SEND_COST_ALERT_EMAIL(alert_rec.ALERT_LEVEL, alert_rec.OBJECT_NAME, alert_rec.MESSAGE);
    END FOR;

    RETURN 'Budget check completed: ' || TO_VARCHAR(:v_alert_count) || ' alerts generated';
END;

-- ----------------------------------------------------------
-- Procedure 5: PROC_REFRESH_COST_DATA
-- Refreshes cost aggregation from source usage history
-- ----------------------------------------------------------
CREATE OR REPLACE PROCEDURE PROC_REFRESH_COST_DATA()
RETURNS VARCHAR
LANGUAGE SQL
EXECUTE AS CALLER
AS
BEGIN
    LET v_rows_inserted NUMBER DEFAULT 0;

    -- Refresh cost data from DATA_QUALITY_MONITORING_USAGE_HISTORY
    -- This pulls actual DQ monitoring costs from the Snowflake system view
    MERGE INTO DQ_COST_DAILY_LOG target
    USING (
        SELECT
            DATE(START_TIME) AS LOG_DATE,
            TABLE_DATABASE AS OBJECT_DATABASE,
            TABLE_SCHEMA AS OBJECT_SCHEMA,
            TABLE_NAME AS OBJECT_NAME,
            SUM(CREDITS_USED) AS CREDITS_USED,
            SUM(CREDITS_USED) * 0.00056 AS ESTIMATED_COST_USD,
            COUNT(*) AS DMF_EXECUTIONS
        FROM SNOWFLAKE.ACCOUNT_USAGE.DATA_QUALITY_MONITORING_USAGE_HISTORY
        WHERE START_TIME >= DATEADD(DAY, -7, CURRENT_TIMESTAMP())
          AND TABLE_DATABASE = 'GSCDNA_DQ'
        GROUP BY DATE(START_TIME), TABLE_DATABASE, TABLE_SCHEMA, TABLE_NAME
    ) source
    ON target.LOG_DATE = source.LOG_DATE
       AND target.OBJECT_NAME = source.OBJECT_NAME
    WHEN MATCHED THEN UPDATE SET
        target.CREDITS_USED = source.CREDITS_USED,
        target.ESTIMATED_COST_USD = source.ESTIMATED_COST_USD,
        target.DMF_EXECUTIONS = source.DMF_EXECUTIONS
    WHEN NOT MATCHED THEN INSERT (LOG_DATE, OBJECT_DATABASE, OBJECT_SCHEMA, OBJECT_NAME, CREDITS_USED, ESTIMATED_COST_USD, DMF_EXECUTIONS)
    VALUES (source.LOG_DATE, source.OBJECT_DATABASE, source.OBJECT_SCHEMA, source.OBJECT_NAME, source.CREDITS_USED, source.ESTIMATED_COST_USD, source.DMF_EXECUTIONS);

    -- Update forecast table
    INSERT INTO DQ_COST_FORECAST (FORECAST_MONTH, PROJECTED_CREDITS, PROJECTED_COST_USD, GROWTH_RATE_PCT, GENERATED_TS)
    SELECT
        DATEADD(MONTH, 1, DATE_TRUNC('MONTH', CURRENT_DATE())),
        ROUND(SUM(CREDITS_USED) * 1.05, 4),
        ROUND(SUM(ESTIMATED_COST_USD) * 1.05, 6),
        5.0,
        CURRENT_TIMESTAMP()
    FROM DQ_COST_DAILY_LOG
    WHERE LOG_DATE >= DATE_TRUNC('MONTH', CURRENT_DATE())
      AND LOG_DATE < CURRENT_DATE();

    RETURN 'Cost data refresh completed at ' || TO_VARCHAR(CURRENT_TIMESTAMP());
END;

