SELECT
    DATEADD(DAY, -SEQ4(), CURRENT_DATE()) AS LOG_DATE,
    'GSCDNA_DQ' AS OBJECT_DATABASE,
    'GSCDNA_DATA_GOV' AS OBJECT_SCHEMA,
    OBJ.OBJECT_NAME,
    ROUND(OBJ.BASE_CREDITS * (1 + (UNIFORM(-30, 30, RANDOM()) / 100.0)), 8) AS CREDITS_USED,
    ROUND(OBJ.BASE_CREDITS * (1 + (UNIFORM(-30, 30, RANDOM()) / 100.0)) * 0.00056, 6) AS ESTIMATED_COST_USD,
    OBJ.BASE_EXECUTIONS + UNIFORM(-2, 5, RANDOM()) AS DMF_EXECUTIONS,
    GREATEST(0, UNIFORM(-5, 15, RANDOM())) AS ERRORS_FOUND
