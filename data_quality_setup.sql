    PASS_FLAG BOOLEAN NOT NULL DEFAULT TRUE,
    MEASUREMENT_TIME TIMESTAMP_NTZ,
    COMMENT = 'DMF execution results for quality tracking'
);

-- Insert execution results for last 7 days across objects
INSERT INTO DQ_EXECUTION_RESULTS (EXECUTION_TS, OBJECT_NAME, METRIC_NAME, METRIC_VALUE, PASS_FLAG, MEASUREMENT_TIME)
SELECT
    DATEADD(HOUR, -SEQ4(), CURRENT_TIMESTAMP()) AS EXECUTION_TS,
    OBJ.OBJECT_NAME,
    MET.METRIC_NAME,
    CASE
        WHEN MET.METRIC_NAME = 'NULL_COUNT' THEN UNIFORM(0, 50, RANDOM())
        WHEN MET.METRIC_NAME = 'DUPLICATE_COUNT' THEN UNIFORM(0, 10, RANDOM())
        WHEN MET.METRIC_NAME = 'FRESHNESS_HOURS' THEN ROUND(UNIFORM(0, 48, RANDOM()), 2)
        WHEN MET.METRIC_NAME = 'ROW_COUNT' THEN UNIFORM(10000, 5000000, RANDOM())
        ELSE ROUND(UNIFORM(0, 100, RANDOM()), 2)
    END AS METRIC_VALUE,
    CASE WHEN UNIFORM(0, 100, RANDOM()) > 15 THEN TRUE ELSE FALSE END AS PASS_FLAG,
    DATEADD(HOUR, -SEQ4(), CURRENT_TIMESTAMP()) AS MEASUREMENT_TIME
FROM TABLE(GENERATOR(ROWCOUNT => 168)) AS G
CROSS JOIN (
    SELECT 'GSCDNA_CUSTOMER_PROFILE' AS OBJECT_NAME UNION ALL
    SELECT 'GSCDNA_SERVICE_ORDER' UNION ALL
    SELECT 'GSCDNA_BILLING_DETAIL' UNION ALL
    SELECT 'GSCDNA_NETWORK_INVENTORY' UNION ALL
    SELECT 'GSCDNA_USAGE_METRICS'
) AS OBJ
CROSS JOIN (
    SELECT 'NULL_COUNT' AS METRIC_NAME UNION ALL
    SELECT 'DUPLICATE_COUNT' UNION ALL
    SELECT 'FRESHNESS_HOURS' UNION ALL
    SELECT 'ROW_COUNT'
) AS MET;

