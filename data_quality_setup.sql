    RESULT_ID NUMBER AUTOINCREMENT PRIMARY KEY,
    EXECUTION_TS TIMESTAMP_NTZ NOT NULL DEFAULT CURRENT_TIMESTAMP(),
    OBJECT_NAME VARCHAR(256) NOT NULL,
    METRIC_NAME VARCHAR(256) NOT NULL,
