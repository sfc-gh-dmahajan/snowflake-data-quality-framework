-- ----------------------------------------------------------
-- View 1: VW_DQ_COST_EXECUTIVE_SUMMARY
-- Executive-level cost summary with KPIs
-- ----------------------------------------------------------
CREATE OR REPLACE VIEW VW_DQ_COST_EXECUTIVE_SUMMARY AS
SELECT
    -- Today's cost
    COALESCE(SUM(CASE WHEN LOG_DATE = CURRENT_DATE() THEN ESTIMATED_COST_USD END), 0) AS TODAY_COST_USD,
    -- Yesterday's cost
    COALESCE(SUM(CASE WHEN LOG_DATE = DATEADD(DAY, -1, CURRENT_DATE()) THEN ESTIMATED_COST_USD END), 0) AS YESTERDAY_COST_USD,
    -- 7-day total cost
    COALESCE(SUM(CASE WHEN LOG_DATE >= DATEADD(DAY, -7, CURRENT_DATE()) THEN ESTIMATED_COST_USD END), 0) AS COST_7D_USD,
    -- 30-day total cost
    COALESCE(SUM(ESTIMATED_COST_USD), 0) AS COST_30D_USD,
    -- 7-day average daily cost
    COALESCE(SUM(CASE WHEN LOG_DATE >= DATEADD(DAY, -7, CURRENT_DATE()) THEN ESTIMATED_COST_USD END) / NULLIF(DATEDIFF(DAY, DATEADD(DAY, -7, CURRENT_DATE()), CURRENT_DATE()), 0), 0) AS AVG_DAILY_COST_7D_USD,
    -- Budget utilization (sum of daily costs / sum of daily budgets)
    ROUND(
        COALESCE(SUM(CASE WHEN LOG_DATE = CURRENT_DATE() THEN ESTIMATED_COST_USD END), 0) /
        NULLIF((SELECT SUM(DAILY_BUDGET_USD) FROM DQ_COST_BUDGET_CONFIG), 0) * 100,
    2) AS BUDGET_UTILIZATION_PCT,
    -- Active anomaly count
    (SELECT COUNT(*) FROM DQ_COST_ANOMALY_LOG WHERE IS_RESOLVED = FALSE) AS ACTIVE_ANOMALIES,
    -- Total objects monitored
    (SELECT COUNT(*) FROM DQ_OBJECTS_MONITORED WHERE IS_ACTIVE = TRUE) AS OBJECTS_MONITORED,
    -- Day over day change percentage
    ROUND(
        (COALESCE(SUM(CASE WHEN LOG_DATE = CURRENT_DATE() THEN ESTIMATED_COST_USD END), 0) -
         COALESCE(SUM(CASE WHEN LOG_DATE = DATEADD(DAY, -1, CURRENT_DATE()) THEN ESTIMATED_COST_USD END), 0)) /
        NULLIF(COALESCE(SUM(CASE WHEN LOG_DATE = DATEADD(DAY, -1, CURRENT_DATE()) THEN ESTIMATED_COST_USD END), 0), 0) * 100,
    2) AS DAY_OVER_DAY_CHANGE_PCT
FROM DQ_COST_DAILY_LOG
WHERE LOG_DATE >= DATEADD(DAY, -30, CURRENT_DATE());

-- ----------------------------------------------------------
-- View 2: VW_DQ_COST_TRENDS
-- 30-day daily cost trend with day-over-day change
-- ----------------------------------------------------------
CREATE OR REPLACE VIEW VW_DQ_COST_TRENDS AS
SELECT
    LOG_DATE,
    SUM(CREDITS_USED) AS TOTAL_CREDITS,
    SUM(ESTIMATED_COST_USD) AS TOTAL_COST_USD,
    COUNT(DISTINCT OBJECT_NAME) AS OBJECTS_COUNT,
    SUM(DMF_EXECUTIONS) AS TOTAL_EXECUTIONS,
    SUM(ERRORS_FOUND) AS TOTAL_ERRORS,
    LAG(SUM(ESTIMATED_COST_USD)) OVER (ORDER BY LOG_DATE) AS PREV_DAY_COST_USD,
    ROUND(
        (SUM(ESTIMATED_COST_USD) - LAG(SUM(ESTIMATED_COST_USD)) OVER (ORDER BY LOG_DATE)) /
        NULLIF(LAG(SUM(ESTIMATED_COST_USD)) OVER (ORDER BY LOG_DATE), 0) * 100,
    2) AS DAY_OVER_DAY_CHANGE_PCT
FROM DQ_COST_DAILY_LOG
WHERE LOG_DATE >= DATEADD(DAY, -30, CURRENT_DATE())
GROUP BY LOG_DATE
ORDER BY LOG_DATE;

-- ----------------------------------------------------------
-- View 3: VW_DQ_COST_BY_PRIORITY
-- Cost grouped by priority tier with object counts
-- ----------------------------------------------------------
CREATE OR REPLACE VIEW VW_DQ_COST_BY_PRIORITY AS
SELECT
    o.PRIORITY_TIER,
    pt.DESCRIPTION AS TIER_DESCRIPTION,
    pt.MAX_BUDGET_DAILY_USD,
    COUNT(DISTINCT o.TABLE_NAME) AS OBJECT_COUNT,
    COALESCE(SUM(c.ESTIMATED_COST_USD), 0) AS TOTAL_COST_30D_USD,
    COALESCE(AVG(c.ESTIMATED_COST_USD), 0) AS AVG_DAILY_COST_USD,
    COALESCE(SUM(c.DMF_EXECUTIONS), 0) AS TOTAL_EXECUTIONS,
    ROUND(
        COALESCE(SUM(c.ESTIMATED_COST_USD), 0) /
        NULLIF((SELECT SUM(ESTIMATED_COST_USD) FROM DQ_COST_DAILY_LOG WHERE LOG_DATE >= DATEADD(DAY, -30, CURRENT_DATE())), 0) * 100,
    2) AS COST_SHARE_PCT
FROM DQ_OBJECTS_MONITORED o
LEFT JOIN DQ_PRIORITY_TIERS pt ON o.PRIORITY_TIER = pt.TIER_NAME
LEFT JOIN DQ_COST_DAILY_LOG c ON o.TABLE_NAME = c.OBJECT_NAME
    AND c.LOG_DATE >= DATEADD(DAY, -30, CURRENT_DATE())
WHERE o.IS_ACTIVE = TRUE
GROUP BY o.PRIORITY_TIER, pt.DESCRIPTION, pt.MAX_BUDGET_DAILY_USD
ORDER BY o.PRIORITY_TIER;

-- ----------------------------------------------------------
-- View 4: VW_DQ_COST_ANOMALIES
-- Anomaly detection using 2+ std dev from 7-day rolling mean
-- ----------------------------------------------------------
CREATE OR REPLACE VIEW VW_DQ_COST_ANOMALIES AS
WITH daily_stats AS (
    SELECT
        OBJECT_NAME,
        LOG_DATE,
        ESTIMATED_COST_USD,
        AVG(ESTIMATED_COST_USD) OVER (
            PARTITION BY OBJECT_NAME
            ORDER BY LOG_DATE
            ROWS BETWEEN 7 PRECEDING AND 1 PRECEDING
        ) AS ROLLING_7D_AVG,
        STDDEV(ESTIMATED_COST_USD) OVER (
            PARTITION BY OBJECT_NAME
            ORDER BY LOG_DATE
            ROWS BETWEEN 7 PRECEDING AND 1 PRECEDING
        ) AS ROLLING_7D_STDDEV
    FROM DQ_COST_DAILY_LOG
    WHERE LOG_DATE >= DATEADD(DAY, -30, CURRENT_DATE())
)
SELECT
    OBJECT_NAME,
    LOG_DATE,
    ESTIMATED_COST_USD AS ACTUAL_COST_USD,
    ROLLING_7D_AVG AS EXPECTED_COST_USD,
    ROLLING_7D_STDDEV,
    CASE
        WHEN ROLLING_7D_STDDEV > 0 THEN
            ROUND((ESTIMATED_COST_USD - ROLLING_7D_AVG) / ROLLING_7D_STDDEV, 2)
        ELSE 0
    END AS DEVIATION_SCORE,
    CASE
        WHEN ROLLING_7D_STDDEV > 0 AND (ESTIMATED_COST_USD - ROLLING_7D_AVG) / ROLLING_7D_STDDEV >= 3 THEN 'CRITICAL'
        WHEN ROLLING_7D_STDDEV > 0 AND (ESTIMATED_COST_USD - ROLLING_7D_AVG) / ROLLING_7D_STDDEV >= 2 THEN 'WARNING'
        ELSE 'NORMAL'
    END AS ANOMALY_SEVERITY,
    CASE
        WHEN ROLLING_7D_STDDEV > 0 AND (ESTIMATED_COST_USD - ROLLING_7D_AVG) / ROLLING_7D_STDDEV >= 2 THEN TRUE
        ELSE FALSE
    END AS IS_ANOMALY
FROM daily_stats
WHERE ROLLING_7D_AVG IS NOT NULL
ORDER BY LOG_DATE DESC, DEVIATION_SCORE DESC;

-- ----------------------------------------------------------
-- View 5: VW_DQ_COST_VS_QUALITY
-- Correlates cost vs pass rate per object
-- ----------------------------------------------------------
CREATE OR REPLACE VIEW VW_DQ_COST_VS_QUALITY AS
WITH cost_agg AS (
    SELECT
        OBJECT_NAME,
        SUM(ESTIMATED_COST_USD) AS TOTAL_COST_USD,
        SUM(CREDITS_USED) AS TOTAL_CREDITS,
        SUM(DMF_EXECUTIONS) AS TOTAL_EXECUTIONS
    FROM DQ_COST_DAILY_LOG
    WHERE LOG_DATE >= DATEADD(DAY, -30, CURRENT_DATE())
    GROUP BY OBJECT_NAME
),
quality_agg AS (
    SELECT
        OBJECT_NAME,
        COUNT(*) AS TOTAL_CHECKS,
        SUM(CASE WHEN PASS_FLAG = TRUE THEN 1 ELSE 0 END) AS PASSED_CHECKS,
        ROUND(SUM(CASE WHEN PASS_FLAG = TRUE THEN 1 ELSE 0 END) * 100.0 / NULLIF(COUNT(*), 0), 2) AS PASS_RATE_PCT
    FROM DQ_EXECUTION_RESULTS
    WHERE EXECUTION_TS >= DATEADD(DAY, -30, CURRENT_TIMESTAMP())
    GROUP BY OBJECT_NAME
)
SELECT
    c.OBJECT_NAME,
    c.TOTAL_COST_USD,
    c.TOTAL_CREDITS,
    c.TOTAL_EXECUTIONS,
    COALESCE(q.TOTAL_CHECKS, 0) AS TOTAL_CHECKS,
    COALESCE(q.PASSED_CHECKS, 0) AS PASSED_CHECKS,
    COALESCE(q.PASS_RATE_PCT, 0) AS PASS_RATE_PCT,
    CASE WHEN c.TOTAL_EXECUTIONS > 0 THEN ROUND(c.TOTAL_COST_USD / c.TOTAL_EXECUTIONS, 6) ELSE 0 END AS COST_PER_EXECUTION_USD,
    CASE WHEN c.TOTAL_CREDITS > 0 THEN ROUND(COALESCE(q.PASS_RATE_PCT, 0) / c.TOTAL_CREDITS, 4) ELSE 0 END AS QUALITY_PER_CREDIT,
    -- Performance quadrant classification
    CASE
        WHEN c.TOTAL_COST_USD <= (SELECT AVG(TOTAL_COST_USD) FROM cost_agg) AND COALESCE(q.PASS_RATE_PCT, 0) >= 85 THEN 'EFFICIENT'
        WHEN c.TOTAL_COST_USD > (SELECT AVG(TOTAL_COST_USD) FROM cost_agg) AND COALESCE(q.PASS_RATE_PCT, 0) >= 85 THEN 'HIGH_COST_HIGH_QUALITY'
        WHEN c.TOTAL_COST_USD <= (SELECT AVG(TOTAL_COST_USD) FROM cost_agg) AND COALESCE(q.PASS_RATE_PCT, 0) < 85 THEN 'LOW_COST_LOW_QUALITY'
        ELSE 'NEEDS_OPTIMIZATION'
    END AS PERFORMANCE_QUADRANT
FROM cost_agg c
LEFT JOIN quality_agg q ON c.OBJECT_NAME = q.OBJECT_NAME
ORDER BY c.TOTAL_COST_USD DESC;

-- ----------------------------------------------------------
-- View 6: VW_DQ_COST_OPTIMIZATION
-- Identifies objects for cost optimization
-- ----------------------------------------------------------
CREATE OR REPLACE VIEW VW_DQ_COST_OPTIMIZATION AS
WITH cost_stats AS (
    SELECT
        OBJECT_NAME,
        AVG(ESTIMATED_COST_USD) AS AVG_DAILY_COST,
        STDDEV(ESTIMATED_COST_USD) AS COST_STDDEV,
        MAX(ESTIMATED_COST_USD) AS MAX_DAILY_COST,
        MIN(ESTIMATED_COST_USD) AS MIN_DAILY_COST,
        SUM(ESTIMATED_COST_USD) AS TOTAL_COST_30D,
        AVG(DMF_EXECUTIONS) AS AVG_DAILY_EXECUTIONS,
        -- Weekend vs weekday cost
        AVG(CASE WHEN DAYOFWEEK(LOG_DATE) IN (0, 6) THEN ESTIMATED_COST_USD END) AS AVG_WEEKEND_COST,
        AVG(CASE WHEN DAYOFWEEK(LOG_DATE) NOT IN (0, 6) THEN ESTIMATED_COST_USD END) AS AVG_WEEKDAY_COST
    FROM DQ_COST_DAILY_LOG
    WHERE LOG_DATE >= DATEADD(DAY, -30, CURRENT_DATE())
    GROUP BY OBJECT_NAME
),
quality_info AS (
    SELECT
        OBJECT_NAME,
        ROUND(SUM(CASE WHEN PASS_FLAG THEN 1 ELSE 0 END) * 100.0 / NULLIF(COUNT(*), 0), 2) AS PASS_RATE_PCT
    FROM DQ_EXECUTION_RESULTS
    WHERE EXECUTION_TS >= DATEADD(DAY, -30, CURRENT_TIMESTAMP())
    GROUP BY OBJECT_NAME
)
SELECT
    cs.OBJECT_NAME,
    cs.AVG_DAILY_COST,
    cs.COST_STDDEV,
    cs.TOTAL_COST_30D,
    cs.AVG_DAILY_EXECUTIONS,
    COALESCE(qi.PASS_RATE_PCT, 0) AS PASS_RATE_PCT,
    -- Cost variability coefficient
    CASE WHEN cs.AVG_DAILY_COST > 0 THEN ROUND(cs.COST_STDDEV / cs.AVG_DAILY_COST, 4) ELSE 0 END AS VARIABILITY_COEFFICIENT,
    -- Weekend utilization ratio
    CASE WHEN cs.AVG_WEEKDAY_COST > 0 THEN ROUND(COALESCE(cs.AVG_WEEKEND_COST, 0) / cs.AVG_WEEKDAY_COST, 4) ELSE 0 END AS WEEKEND_UTILIZATION_RATIO,
    -- Potential savings if weekend checks reduced by 50%
    ROUND(COALESCE(cs.AVG_WEEKEND_COST, 0) * 0.5 * 8, 6) AS POTENTIAL_WEEKEND_SAVINGS_USD,
    -- Optimization recommendation
    CASE
        WHEN COALESCE(qi.PASS_RATE_PCT, 0) > 99 AND cs.AVG_DAILY_COST > 0.015 THEN 'REDUCE_FREQUENCY - High pass rate suggests less frequent checks needed'
        WHEN cs.COST_STDDEV / NULLIF(cs.AVG_DAILY_COST, 0) > 0.5 THEN 'INVESTIGATE_VARIABILITY - High cost variability indicates unstable workload'
        WHEN COALESCE(cs.AVG_WEEKEND_COST, 0) > cs.AVG_WEEKDAY_COST * 0.8 THEN 'REDUCE_WEEKEND - Weekend costs nearly equal weekday; consider reducing'
        WHEN COALESCE(qi.PASS_RATE_PCT, 0) < 70 AND cs.AVG_DAILY_COST < 0.010 THEN 'INCREASE_MONITORING - Low quality with low spend; increase checks'
        ELSE 'OPTIMAL - Current configuration is cost-effective'
    END AS OPTIMIZATION_RECOMMENDATION
FROM cost_stats cs
LEFT JOIN quality_info qi ON cs.OBJECT_NAME = qi.OBJECT_NAME
ORDER BY cs.TOTAL_COST_30D DESC;

