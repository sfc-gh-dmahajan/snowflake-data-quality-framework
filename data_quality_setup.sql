-- ----------------------------------------------------------
-- Procedure 1: PROC_CHECK_COST_ANOMALIES
-- Checks for cost spikes using statistical analysis
-- ----------------------------------------------------------
CREATE OR REPLACE PROCEDURE PROC_CHECK_COST_ANOMALIES()
RETURNS VARCHAR
LANGUAGE SQL
EXECUTE AS CALLER
AS
BEGIN
    -- Detect anomalies: cost > 2 std dev above 7-day rolling mean
    INSERT INTO DQ_COST_ANOMALY_LOG (ANOMALY_DATE, OBJECT_NAME, EXPECTED_COST, ACTUAL_COST, DEVIATION_STDDEV, SEVERITY, IS_RESOLVED, DETECTED_TS)
    SELECT
        a.LOG_DATE,
        a.OBJECT_NAME,
        a.EXPECTED_COST_USD,
        a.ACTUAL_COST_USD,
        a.DEVIATION_SCORE,
        a.ANOMALY_SEVERITY,
        FALSE,
        CURRENT_TIMESTAMP()
    FROM VW_DQ_COST_ANOMALIES a
    WHERE a.IS_ANOMALY = TRUE
      AND a.LOG_DATE = CURRENT_DATE()
      AND NOT EXISTS (
          SELECT 1 FROM DQ_COST_ANOMALY_LOG al
          WHERE al.ANOMALY_DATE = a.LOG_DATE
            AND al.OBJECT_NAME = a.OBJECT_NAME
      );

    -- Generate alerts for new anomalies
    INSERT INTO DQ_COST_ALERTS_HISTORY (ALERT_TS, ALERT_TYPE, ALERT_LEVEL, OBJECT_NAME, MESSAGE, EMAIL_SENT)
    SELECT
        CURRENT_TIMESTAMP(),
        'COST_ANOMALY',
        SEVERITY,
        OBJECT_NAME,
        'Cost anomaly detected: actual $' || TO_VARCHAR(ACTUAL_COST, '999,999.999999') ||
        ' vs expected $' || TO_VARCHAR(EXPECTED_COST, '999,999.999999') ||
        ' (' || TO_VARCHAR(DEVIATION_STDDEV) || ' std dev)',
        FALSE
    FROM DQ_COST_ANOMALY_LOG
    WHERE ANOMALY_DATE = CURRENT_DATE()
      AND DETECTED_TS >= DATEADD(MINUTE, -5, CURRENT_TIMESTAMP());

    RETURN 'Anomaly check completed at ' || TO_VARCHAR(CURRENT_TIMESTAMP());
END;

